/* Описание скрипта
Для реализации описанного функционала, мы создадим JavaScript код, который будет обрабатывать событие клика по кнопке с классом .btn. Скрипт будет проверять содержимое текстового поля на наличие фрагмента "код 2025". В зависимости от результата проверки, будет выводиться соответствующее сообщение в окне оповещения и очищаться текстовое поле.
Шаги для реализации
1.	Получить элементы: Получим элементы текстового поля и кнопки.
2.	Добавить обработчик события: Добавим обработчик события клика на кнопку.
3.	Проверка текста: Внутри обработчика проверим, содержит ли текст "код 2025".
4.	Вывод сообщения: В зависимости от результата проверки, выведем соответствующее сообщение и очистим текстовое поле.
 */

// Получаем элементы
const button = document.querySelector(".btn");
const textarea = document.getElementById("exampleFormControlInput1");

// Добавляем обработчик события клика
button.addEventListener("click", function () {
  // Получаем текст из текстового поля
  const userInput = textarea.value;

  // Проверяем наличие фрагмента "код 2025"
  if (userInput.includes("код 2025")) {
    alert("Письмо отправлено!");
  } else {
    alert("Код введён неверный! Пройди квест для получения кода!");
  }

  // Очищаем текстовое поле
  textarea.value = "";
});
